package abstraction;

public class AbstractionDocs {
	/*
	 * ---------- Abstraction ----------------
	 * # process of hiding implementation details in the program 
	 * 	 is known as abstraction.
	 * 
	 * # To achieve abstraction :
	 * 
	 * a. Abstract class : 
	 * 
	 * # Class which contains abstract (unimplemented) methods and non-abstract
	 *   method is known as Abstract class.
	 * 	  
	 *   # Abstract method :
	 *   
	 *     - method which does not have implementation details/body.
	 *      e.g.  
	 *      abstract void print();
	 *      abstract int getArea(int l, int b);
	 *      
	 *     # every abstract class must have at least one child class and 
	 *       child class must override abstract methods.
	 *     # we can't create object of abstract class.
	 *     # level of abstraction 0-100%
	 *     
	 *     # Syntax: 
	 *     
	 *     		abstract class class_name {
	 *     				// abstract methods
	 *     
	 *     				// non-abstract methods
	 *     			}
	 *     
	 * 
	 * b. Interface : 
	 * 
	 * # same as class which contains public abstract  methods and 
	 *   public static final variables only.
	 * # every interface must have at least one implementation class and impl. class must 
	 *   override abstract methods.
	 * # we can't create object of interface.
	 * # level of abstraction 100%.
	 * 
	 * # Syntax :
	 * 		
	 * 		interface interface_name{
	 * 
	 * 		//public abstract methods
	 * 
	 * 		}
	 * 
	 * 
	 * 
	 */

}
