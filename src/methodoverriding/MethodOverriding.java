package methodoverriding;

public class MethodOverriding {
	/*
	 * ------- Method Overriding --------
	 * # use to achieve run time polymorphism.
	 * # there must be inheritance between classes.
	 * # method name, return type and list of arguments same as 
	 * 	parent class but implementation is different in child class 
	 * 	is known as method overriding.
	 * 
	 * 	* inherit : child class use same as parent class
	 * 	* override : change parent class method in child class
	 * 	
	 */
}
